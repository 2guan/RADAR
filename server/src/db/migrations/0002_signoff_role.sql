-- ============================================================================
-- 文件：0002_signoff_role.sql
-- 用途：为角色表增加"是否会签角色"标识。打标的角色会出现在投产管理的评审会签中，
--       且仅具备对应角色的人员可执行签署/驳回。
-- 作者：hengguan
-- ============================================================================

ALTER TABLE role ADD COLUMN is_signoff_role INTEGER NOT NULL DEFAULT 0;

-- 默认将业务/开发/测试/运维（金科侧）标记为会签角色，管理员可在系统设置中调整
UPDATE role SET is_signoff_role = 1
 WHERE code IN ('金科业务', '金科开发', '金科测试', '金科运维');
